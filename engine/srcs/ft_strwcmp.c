#include <engine.h>

int			ft_strwcmp(const char *s1, const char *s2)
{
	size_t	i1;
	size_t	i2;

	i1 = 0;
	i2 = 0;
	while (s1[i1] == ' ' || s1[i1] == '\t')
		i1++;
	while (s2[i2] == ' ' || s2[i2] == '\t')
		i2++;
	while ((s1[i1] != ' ' && s1[i1] != '\t' && s1[i1] != '\0')
		&& (s2[i2] != ' ' && s2[i2] != '\t' && s2[i2] != '\0'))
	{
		if (s1[i1] != s2[i2])
			return ((unsigned char)s1[i1] - (unsigned char)s2[i2]);
		i1++;
		i2++;
	}
	return (0);
}
