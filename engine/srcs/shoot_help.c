#include <engine.h>

void	point_swapper_shoot(float *t0, float *t1, float a, float b)
{
	if (a > b)
	{
		*t0 = a;
		*t1 = b;
	}
	else
	{
		*t1 = a;
		*t0 = b;
	}
}
